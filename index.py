# Define the code as a function
def example_task():
    print("Hello, Batch Processing!")
    # Example task: Add two numbers
    result = 5 + 3
    print(f"The result is: {result}")
    print("-" * 30)
# Batch of three sets of the same code
if __name__ == "__main__":
    print("Executing batch 1:")
    example_task()
